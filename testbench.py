_SUITE_RUNNER = r'''
import json, sys, types, contextlib
d = json.loads(sys.stdin.read())
_NONCE = d.get("nonce", "")
_SUITE_SRC = d["suite"]
def _emit(passed, n):
    # the parent trusts ONLY the verdict line carrying this per-call nonce, so an
    # untrusted suite that prints a forged {"passed": true} ledger is ignored.
    print(json.dumps({"passed": bool(passed), "n": n, "nonce": _NONCE}))
ns = {}
try:
    exec(d["impl"], ns)
except BaseException:
    pass  # tests calling the (now-undefined) function will fail -> mutant killed
del d  # FRAME ISOLATION: the impl/mutant source now lives only as a function object in ns.
       # A suite that walks the call stack (f_back -> f_globals) finds no source text to
       # build a pass-iff-reference oracle. This is the real defense; the AST guard is a sieve.

# Agents habitually write `from <fn> import <fn>` / `from solution import <fn>` and
# `import pytest`. Make the impl's functions importable and stub pytest so those habits
# don't crash the gate (the reward should reflect TEST QUALITY, not import style).
try:
    _defined = {k: v for k, v in list(ns.items()) if callable(v) and not k.startswith("__")}
    _sol = types.ModuleType("solution")
    for _k, _v in _defined.items():
        setattr(_sol, _k, _v)
    sys.modules["solution"] = _sol
    for _k in _defined:
        _m = types.ModuleType(_k)
        for _k2, _v2 in _defined.items():
            setattr(_m, _k2, _v2)
        sys.modules[_k] = _m
    try:
        import pytest  # noqa: F401
    except Exception:
        _pt = types.ModuleType("pytest")
        @contextlib.contextmanager
        def _raises(exc=Exception, *a, **k):
            try:
                yield
            except exc:
                return
            raise AssertionError("DID NOT RAISE")
        class _Approx:
            def __init__(self, v, rel=1e-6, abs=1e-9):
                self.v, self.rel, self.abs = v, rel, abs
            def __eq__(self, o):
                try:
                    return abs(o - self.v) <= max(self.abs, self.rel * max(abs(o), abs(self.v)))
                except Exception:
                    return o == self.v
        _noop = lambda *a, **k: (a[0] if (a and callable(a[0])) else (lambda f: f))
        _pt.raises = _raises
        _pt.approx = lambda v, rel=1e-6, abs=1e-9: _Approx(v, rel, abs)
        _pt.fixture = _noop
        _pt.mark = types.SimpleNamespace(parametrize=lambda *a, **k: (lambda f: f),
                                         skip=_noop, skipif=lambda *a, **k: (lambda f: f))
        sys.modules["pytest"] = _pt
except Exception:
    pass

try:
    exec(_SUITE_SRC, ns)
except BaseException:
    _emit(False, 0); raise SystemExit
tests = [v for k, v in list(ns.items()) if k.startswith("test_") and callable(v)]
if not tests:
    _emit(False, 0); raise SystemExit
for t in tests:
    try:
        t()
    except BaseException:
        _emit(False, len(tests)); raise SystemExit
_emit(True, len(tests))
'''

_EVAL_RUNNER = r'''
import json, sys
d = json.loads(sys.stdin.read())
ns = {}
try:
    exec(d["impl"], ns)
except Exception:
    print(json.dumps({"sig": ["IMPORT_ERR"] * len(d["inputs"])})); raise SystemExit
fn = ns.get(d["func"])
if not callable(fn):
    print(json.dumps({"sig": ["NO_FUNC"] * len(d["inputs"])})); raise SystemExit
sig = []
for args in d["inputs"]:
    try:
        v = fn(*args)
        try:
            sig.append("OK:" + json.dumps(v, sort_keys=True))
        except Exception:
            sig.append("OK:" + repr(v))
    except Exception as e:
        sig.append("ERR:" + type(e).__name__)
print(json.dumps({"sig": sig}))
'''


def _run_suite_local(impl_src: str, suite_src: str, timeout: float = 4.0) -> bool:
    """True iff the suite PASSES against this implementation (local subprocess).

    The verdict is authenticated by a per-call nonce: the untrusted suite cannot know
    it (introspection that could read it is blocked by _suite_security_violation), so a
    forged {"passed": true} stdout ledger is ignored — only the harness's nonce-stamped
    line is trusted. No authenticated verdict (forged / crashed / silent) => not a pass."""
    nonce = hashlib.sha256(os.urandom(16)).hexdigest()
    payload = json.dumps({"impl": impl_src, "suite": suite_src, "nonce": nonce})
    try:
        proc = subprocess.run([sys.executable, "-c", _SUITE_RUNNER], input=payload,
                              capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        return False  # a hanging impl/test => not a pass (so a hanging mutant is killed)
    for line in reversed((proc.stdout or "").splitlines()):
        line = line.strip()
        if not line:
            continue
        try:
            obj = json.loads(line)
        except Exception:
            continue
        if isinstance(obj, dict) and obj.get("nonce") == nonce:
            return bool(obj.get("passed"))
    return False


def _run_suite_once(impl_src: str, suite_src: str, timeout: float = 4.0) -> bool:
    """Run a suite against one implementation. Routes to a sandbox runner when
    REWARDFORGE_RUNNER is set (daytona | modal); otherwise the local subprocess.
    Both runners expose the same (impl, suite) -> bool interface."""
    runner = os.environ.get("REWARDFORGE_RUNNER", "local")
    if runner == "daytona":
        import daytona_runner
        return daytona_runner.run_suite_once(impl_src, suite_src, timeout)
    if runner == "modal":
        import modal_runner
        return modal_runner.run_suite_once(impl_src, suite_src, timeout)
    return _run_suite_local(impl_src, suite_src, timeout)


def _eval_signature(impl_src: str, func: str, inputs: list, timeout: float = 4.0) -> list:
    payload = json.dumps({"impl": impl_src, "func": func, "inputs": inputs})
    try:
        proc = subprocess.run([sys.executable, "-c", _EVAL_RUNNER], input=payload,
                              capture_output=True, text=True, timeout=timeout)
    except subprocess.TimeoutExpired:
        return ["TIMEOUT"] * len(inputs)
    out = (proc.stdout or "").strip().splitlines()
    if not out:
        return ["EVAL_ERR"] * len(inputs)
    try:
        return json.loads(out[-1]).get("sig", ["EVAL_ERR"] * len(inputs))
    except Exception:
        return ["EVAL_ERR"] * len(inputs)


