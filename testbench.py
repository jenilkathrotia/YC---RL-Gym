_POOL_CACHE: dict[str, list] = {}
MAX_MUTANTS = 18


def _filter_inputs(module_id: str):
    m = MODULES[module_id]
    rng = random.Random(_stable_seed("inputs:" + module_id))
    inputs = list(m["edges"]) + [m["gen"](rng) for _ in range(30)]
    # normalize tuples -> lists for JSON round-trip (args are passed as a list)
    return [list(args) for args in inputs]


def get_mutant_pool(module_id: str) -> list:
    """Non-equivalent, killable mutants of the reference (cached, deterministic)."""
    if module_id in _POOL_CACHE:
        return _POOL_CACHE[module_id]
    m = MODULES[module_id]
    func = m["func"]
    inputs = _filter_inputs(module_id)
    ref_sig = _eval_signature(m["reference"], func, inputs)

    candidates = _gen_ast_mutants(m["reference"]) + list(m["extra_mutants"])
    seen, pool = set(), []
    for src in candidates:
        if src == m["reference"] or src in seen:
            continue
        seen.add(src)
        if _eval_signature(src, func, inputs) != ref_sig:   # non-equivalent => killable
            pool.append(src)
        if len(pool) >= MAX_MUTANTS:
            break
    _POOL_CACHE[module_id] = pool
    return pool


# A test suite is UNTRUSTED code. Imports are ALLOWLISTED, not denylisted — a denylist is a
# sieve: `operator.attrgetter("f_back")` walks the call stack with the attribute name as a
# runtime string the AST never sees, reads the hidden reference out of the harness frame, and
# fakes a perfect score (confirmed by red-team). So only pytest / solution / the function
# under test may be imported; every escape primitive (eval/exec/getattr-class names) and every
# dunder attribute is rejected; literal frame attrs are denied too. Defense-in-depth: the
# runner also deletes the impl source before the suite runs (frame isolation).
_ALLOWED_IMPORTS = {"pytest", "solution"}
# benign dunder *names* a suite may legitimately mention (e.g. `if __name__ == "__main__"`);
# everything else dunder is an escape gadget.
_ALLOWED_DUNDER_NAMES = {"__name__", "__doc__", "__file__", "__spec__", "__loader__", "__package__"}
_DENY_NAMES = {
    "eval", "exec", "compile", "open", "__import__", "globals", "locals", "vars", "dir",
    "getattr", "setattr", "delattr", "input", "breakpoint", "memoryview",
}
_DENY_ATTRS = {  # non-dunder frame/introspection attrs (all dunder attrs are blocked wholesale)
    "f_back", "f_globals", "f_locals", "f_builtins", "f_code", "gi_frame", "cr_frame",
    "ag_frame", "tb_frame", "tb_next", "_getframe", "currentframe", "mro",
}


def _suite_security_violation(suite_src: str) -> str | None:
    """Reason string if the suite tries to escape the sandbox / read hidden state / forge
    the verdict; None if it looks like an ordinary test suite. Cheating scores 0 — the only
    way to score is to write tests that actually catch bugs."""
    try:
        tree = ast.parse(suite_src)
    except SyntaxError:
        return None  # unparseable -> it just fails the gate -> 0 anyway
    allowed = _ALLOWED_IMPORTS | {m["func"] for m in MODULES.values()}
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            for a in node.names:
                if a.name.split(".")[0] not in allowed:
                    return "import:" + a.name
        elif isinstance(node, ast.ImportFrom):
            if (node.module or "").split(".")[0] not in allowed:
                return "import-from:" + (node.module or "<relative>")
        elif isinstance(node, ast.Name):
            dunder = node.id.startswith("__") and node.id.endswith("__")
            if node.id in _DENY_NAMES or (dunder and node.id not in _ALLOWED_DUNDER_NAMES):
                return "name:" + node.id
        elif isinstance(node, ast.Attribute):
            if (node.attr.startswith("__") and node.attr.endswith("__")) or node.attr in _DENY_ATTRS:
                return "attr:" + node.attr
    return None


def score_suite(module_id: str, suite_src: str):
    """Reward = (#mutants killed / #mutants) gated by passing reference + equivalents."""
    viol = _suite_security_violation(suite_src)
    if viol:  # untrusted suite tried to escape / read hidden state / forge the verdict
        return 0.0, {"gate": False, "reason": "security_violation", "detail": viol}
    m = MODULES[module_id]
    gate_impls = [m["reference"]] + m["equivalents"]
    for impl in gate_impls:
        if not _run_suite_once(impl, suite_src):
            return 0.0, {"gate": False, "reason": "suite fails reference or an equivalent refactor"}
    pool = get_mutant_pool(module_id)
    if not pool:
        return 0.0, {"gate": True, "mutants": 0}
    killed = sum(0 if _run_suite_once(mut, suite_src) else 1 for mut in pool)
    return killed / len(pool), {"gate": True, "killed": killed, "mutants": len(pool)}


_TESTBENCH_TEMPLATE = """You are TestBench-Forge. Write a Python test suite for the function below.

MODULE: {func}
{description}

Reference implementation (correct):
```python
{reference}
```

Write one or more functions named `test_*()` that call `{func}(...)` and `assert` correct
behavior. Your suite is scored by the fraction of HIDDEN, freshly-injected bugs (mutants of
the reference) that it CATCHES (a test fails on the buggy version). So probe edge cases,
boundaries, and adversarial inputs — not just the happy path.

Hard rules:
  - `{func}` is ALREADY defined and in scope — call it directly. Do NOT import it
    (no `from ... import {func}`) and do NOT `import pytest`. Write plain top-level
    functions: `def test_x():` with `assert ...`. No fixtures, no parametrize.
  - Your suite MUST pass on the correct reference AND on behavior-equivalent refactors. A
    suite that fails the reference (e.g. `assert False`, or tests tied to internal details)
    scores 0.
  - You cannot see the mutants. Maximize coverage of distinct behaviors to kill unseen bugs.

Reference behavior on a few inputs:
{examples}

Reply with ONLY the Python test code (a ```python block is fine)."""


def build_testbench_prompt(module_id: str) -> str:
    m = MODULES[module_id]
    func = m["func"]
    inputs = m["edges"][:5]
    sig = _eval_signature(m["reference"], func, [list(a) for a in inputs])
    ex = "\n".join(f"  {func}{tuple(a)} -> {s[3:] if s.startswith('OK:') else s}"
                   for a, s in zip(inputs, sig))
    return _TESTBENCH_TEMPLATE.format(
        func=func, description=m["description"], reference=m["reference"].strip(), examples=ex)
